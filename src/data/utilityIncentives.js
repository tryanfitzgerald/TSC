// Incentive tier thresholds (for map coloring)
export const INCENTIVE_TIERS = [
  { min: 400000, color: '#00B4D8', label: 'Exceptional (>$400K)', tier: 'exceptional' },
  { min: 200000, color: '#2EC4B6', label: 'Strong ($200K–$400K)', tier: 'strong' },
  { min: 50000,  color: '#F4A261', label: 'Moderate ($50K–$200K)', tier: 'moderate' },
  { min: 1,      color: '#E76F51', label: 'Limited (<$50K)', tier: 'limited' },
  { min: 0,      color: '#3D5A6E', label: 'No known DCFC program', tier: 'none' },
];

export function getTier(totalIncentive) {
  for (const t of INCENTIVE_TIERS) {
    if (totalIncentive >= t.min) return t;
  }
  return INCENTIVE_TIERS[INCENTIVE_TIERS.length - 1];
}

